

//Kernel = 系统内核 / 核心模块
//Kernel 是嵌入式系统核心模块，负责任务调度、硬件初始化、中断管理和系统资源分配，是控制系统运行的基础框架。


/********************************************************************************
*CONTROL SWITCH									*
********************************************************************************/
// 空闲任务使能开关宏
// 取值说明：0=禁用空闲任务，1=启用空闲任务（原注释"Argument check enable switch"为笔误，应为空闲任务控制）
#define		OS_TASK_IDLE_EN			0	

// 系统表存储位置开关宏
// 取值说明：0=常量数据存储在RAM（内存），1=常量数据存储在FLASH（ROM/闪存）
#define		OS_TABLE_IN_FLASH		1	

// 栈增长方向控制宏
// 取值说明：0=栈向下增长（从高地址到低地址），1=栈向上增长（从低地址到高地址）
#define		OS_STK_GROWTH_UP		1	

// 栈检查功能使能开关宏
// 取值说明：0=禁用栈检查（不检测栈溢出），1=启用栈检查（运行时检测栈使用情况，防止溢出）
#define		OS_STK_CHK_EN		    0	

/********************************************************************************
*STACK CONFIG									*
********************************************************************************/
// 系统最大栈大小宏
// 定义：整个操作系统支持的单个任务栈的最大容量（单位：字节），当前配置为1536字节
#define		OS_MAX_STK_SIZE			1536	

// 任务栈最大尺寸宏（补充配置）
// 定义：单个任务栈的实际最大分配尺寸（单位：字节），当前配置为250字节
#define     cMaxStkSize             250

/********************************************************************************
*TASK CONFIG									*
********************************************************************************/
// 系统最低优先级宏
// 定义：操作系统支持的最低任务优先级（数值越大优先级越低），当前配置为5（对应最大任务数=6）
#define		OS_LOWEST_PRIO			5	

// 条件编译块：仅当OS_TASK_IDLE_EN>0（启用空闲任务）时生效
#if		OS_TASK_IDLE_EN > 0
// 空闲任务优先级宏
// 定义：空闲任务的优先级（需高于OS_LOWEST_PRIO，为系统最低优先级），当前配置为9
#define		OS_IDLE_PRIO			9	

// 空闲任务栈大小宏
// 定义：空闲任务分配的栈容量（单位：字节），当前配置为50字节
#define		OS_TASK_IDLE_STK_SIZE		50	

// CPU使用率计算因子宏
// 定义：用于计算CPU空闲率/使用率的系数（公式：CPU使用率=100% - (空闲计数/OS_CPUFREE_FACTOR)），当前配置为126
#define		OS_CPUFREE_FACTOR		126	
#endif

/********************************************************************************
*EVENT NUMNER DEFINITION							*
********************************************************************************/
// 任务事件类型定义宏
// 定义：将TASK_EVENT别名化为INT16U类型（16位无符号整型），用于标识任务事件编号
#define		TASK_EVENT			INT16U

/********************************************************************************
*TIMER CONFIG									*
********************************************************************************/
// 系统时钟节拍频率宏
// 定义：操作系统1秒内产生的定时器中断（Tick ISR）次数，当前配置为2000次/秒（即500μs触发一次时钟节拍）
#define		OS_TICKS_PER_SEC		2000	


